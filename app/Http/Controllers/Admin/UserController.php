     * @return \Illuminate\Http\Response
     */
    public function storeHamlet(Request $request)
    {
        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => bcrypt($request->password),
        ]);

        $user->assignRole('hamlet');

        return redirect()->route('admin.user.hamlet');
    }

